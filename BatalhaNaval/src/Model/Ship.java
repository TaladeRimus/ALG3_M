package Model;

public class Ship {
	
	private String tipo;
	private String dado;
	private String visual;

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getTipo() {
		return this.tipo;
	}

	public void setDado(String dado) {
		this.dado = dado;
	}

	public String getDado() {
		return this.dado;
	}

	public String getVisual() {
		return this.visual;
	}

	public void setVisual(String visual) {
		this.visual = visual;

	}

}
